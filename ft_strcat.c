char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	b;

	i = 0;
	while (dest[i])
	{
		i++;
	}
	b = 0;
	while (src[b])
	{
		dest[i] = src[b];
		b++;
		i++;
	}
	dest[i] = 0;
	return (dest);
}
